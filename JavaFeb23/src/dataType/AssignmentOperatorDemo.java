package dataType;

public class AssignmentOperatorDemo {

	public static void main(String[] args) {
 

		
		  /**  Assignment Operators are (=, +=, *=, /= and  %=)
	     *  a = 7 means a =7
	     *  a += 7 means a = a+7
	     *  a -= 7 means a = a-7
	     *  a *= 7 means a = a*7
	     *  a /= 7 means a = a/7
	     *  a %= 7 means a = a%7
	     *  Examples; 
	     */
			
		int a = 87;
		int results = a-7;
		results = a/7;
		System.out.println(a+=7); // answer should be 94  
		System.out.println(a-=7);// answer should be 80
		System.out.println(a*=7);// answer should be 609
		System.out.println(results); //
	    System.out.println();//
	}

}
